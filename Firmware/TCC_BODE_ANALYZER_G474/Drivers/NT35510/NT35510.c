void NT35510_Init(void)
{
	/* Perform a hardware reset */
	HAL_GPIO_WritePin(LCD_RST_GPIO_Port, LCD_RST_Pin, GPIO_PIN_SET);
	LCD_Delay(100);
	HAL_GPIO_WritePin(LCD_RST_GPIO_Port, LCD_RST_Pin, GPIO_PIN_RESET);
	LCD_Delay(100);
	HAL_GPIO_WritePin(LCD_RST_GPIO_Port, LCD_RST_Pin, GPIO_PIN_SET);
	LCD_Delay(100);

	NT35510_Gamma_Correction();

	/* DPFRCTR1: Display Timing Control in Normal / Idle Off Mode */
	LCD_WR_REG(0xBD00); LCD_WR_DATA(0x01); //19.45us
	LCD_WR_REG(0xBD01); LCD_WR_DATA(0x84);
	LCD_WR_REG(0xBD02); LCD_WR_DATA(0x07); // 7 lines
	LCD_WR_REG(0xBD03); LCD_WR_DATA(0x31); // 49 lines

	/* Normal Mode On */
	LCD_WR_REG(NT35510_CMD_NORON);

	/* Tearing Effect On */
//	LCD_WR_REG(NT35510_CMD_TEEON);
//	LCD_WR_DATA(0x00);
//
//	/* Set Tear Scan Line Mode */
//	LCD_WR_REG(NT35510_CMD_STESL);
//	LCD_WR_DATA(0x00);
//	LCD_WR_REG(NT35510_CMD_STESL + 1);
//	LCD_WR_DATA(0x00);

	/* Change the color mode to RG565 */
	LCD_WR_REG(NT35510_CMD_COLMOD);
	LCD_WR_DATA(NT35510_COLMOD_RGB565);

//	LCD_WR_REG(NT35510_CMD_MADCTL);
//	LCD_WR_DATA(0x14);

	/* Send the Sleep Out Command */
	LCD_WR_REG(NT35510_CMD_SLPOUT);

	LCD_Delay(120);

	/* Turn the display on */
	LCD_WR_REG(NT35510_CMD_DISPON);

#ifdef DMA_HANDLE
	DMA_HANDLE.XferCpltCallback = DMA_TXCALLBACK;
#endif
}

void NT35510_SetWindow(uint16_t x, uint16_t y, uint16_t sizex, uint16_t sizey)
{
	LCD_WR_REG(NT35510_CMD_CASET);
	LCD_WR_DATA((x & 0xFF00) >> 8);
	LCD_WR_REG((NT35510_CMD_CASET) + 1);
	LCD_WR_DATA(x & 0x00FF);
	LCD_WR_REG((NT35510_CMD_CASET) + 2);
	LCD_WR_DATA(((x + sizex) & 0xFF00) >> 8);
	LCD_WR_REG((NT35510_CMD_CASET) + 3);
	LCD_WR_DATA((x + sizex - 1) & 0x00FF);

	LCD_WR_REG(NT35510_CMD_RASET);
	LCD_WR_DATA((y & 0xFF00) >> 8);
	LCD_WR_REG((NT35510_CMD_RASET) + 1);
	LCD_WR_DATA(y & 0x00FF);
	LCD_WR_REG((NT35510_CMD_RASET) + 2);
	LCD_WR_DATA(((y + sizey) & 0xFF00) >> 8);
	LCD_WR_REG((NT35510_CMD_RASET) + 3);
	LCD_WR_DATA((y + sizey - 1) & 0x00FF);
}

uint16_t NT35510_GetLine(void)
{
	uint32_t line;

	LCD_WR_REG(NT35510_CMD_GSL);
	line = LCD_RD_DATA() << 8;
	LCD_WR_REG(NT35510_CMD_GSL + 1);
	line |= LCD_RD_DATA();

	return line;
}

void NT35510_DMAWrite(uint16_t *pixels, uint16_t x, uint16_t y, uint16_t sizex, uint16_t sizey)
{
	__isTranmittingBlock = SET;

	NT35510_SetWindow(x, y, sizex, sizey);

	DMA_HANDLE.Instance->CMAR = (uint32_t)LCD_RAM;
	DMA_HANDLE.Instance->CPAR = (uint32_t)pixels;
	DMA_HANDLE.Instance->CNDTR = sizex * sizey;

	__HAL_DMA_ENABLE_IT(&DMA_HANDLE, DMA_IT_TC);

	LCD_WR_REG(NT35510_CMD_RAMWR);

	__HAL_DMA_ENABLE(&DMA_HANDLE);
}

